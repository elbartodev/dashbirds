def calcular_autosomal_recessive(geno_pai, geno_mae):
    alelos = {
        "normal": ["+", "+"],
        "split":  ["+", "m"],
        "visual": ["m", "m"],
    }
    pai_alelos = alelos.get(geno_pai, ["+", "+"])
    mae_alelos = alelos.get(geno_mae, ["+", "+"])

    combinacoes = []
    for p in pai_alelos:
        for m in mae_alelos:
            par = tuple(sorted([p, m], reverse=True))
            combinacoes.append(par)

    contagem = {"normal": 0, "split": 0, "visual": 0}
    for par in combinacoes:
        if par == ("+", "+"):
            contagem["normal"] += 1
        elif par == ("+", "m"):
            contagem["split"] += 1
        elif par == ("m", "m"):
            contagem["visual"] += 1

    total = sum(contagem.values())
    fenotipos = {
        "normal": {"pt": "Normal", "en": "Normal", "fr": "Normal", "de": "Normal"},
        "split":  {"pt": "Normal/Split (portador)", "en": "Normal/Split (carrier)", "fr": "Normal/Split (porteur)", "de": "Normal/Split (Träger)"},
        "visual": {"pt": "Visual (mutação expressa)", "en": "Visual (mutation expressed)", "fr": "Visuel (mutation exprimée)", "de": "Visuell (Mutation exprimiert)"},
    }
    genotipos_label = {"normal": "+/+", "split": "+/m", "visual": "m/m"}

    resultado = []
    for tipo, qtd in contagem.items():
        if qtd > 0:
            resultado.append({
                "fenotipo": fenotipos[tipo],
                "genotipo": genotipos_label[tipo],
                "probabilidade": round((qtd / total) * 100, 1),
                "fracao": f"{qtd}/{total}",
                "sexo": "ambos"
            })
    return resultado


